const FogOfWarMasks = {
  getMaskBySize(size) {
    const oneByOne = [[1]];
    const offset = 1;
    if (!size || size < offset) return oneByOne;
    if (!masks[size - offset]) return oneByOne;
    return masks[size - offset];
  },

  getVisionScreenWidth(size) {
    const mask = this.getMaskBySize(size);
    return Math.round(mask.length / 2) * TILE_WIDTH;
  },
};

